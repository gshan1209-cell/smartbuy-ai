CREATE OR REPLACE FUNCTION public.set_mutual_aid_posts_updated_at()
RETURNS trigger AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS mutual_aid_posts_set_updated_at ON public.mutual_aid_posts;
CREATE TRIGGER mutual_aid_posts_set_updated_at
    BEFORE UPDATE ON public.mutual_aid_posts
    FOR EACH ROW
    EXECUTE FUNCTION public.set_mutual_aid_posts_updated_at();

